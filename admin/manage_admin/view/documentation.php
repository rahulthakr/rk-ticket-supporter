                     <div class="page-header">
                        <h3>		Installation</h3>
                        <hr class="notop">
                     </div>
                     <h4 id="installation_wordpress_version">WordPress Version:</h4>
                     <p>	You have to use WordPress version 5.0+.</p>
                     <h4 id="installation_to_run_wordpress_your_host_just_needs_a_couple_of_things">To run WordPress your host just needs a couple of things:</h4>
                     <ul>
                        <li>		<a href="http://www.php.net/">PHP</a> version 5.2.4 or greater (recommended: PHP 5.4 or greater)</li>
                        <li>		<a href="http://www.mysql.com/">MySQL</a> version 5.0 or greater (recommended: MySQL 5.5 or greater)<br>		&nbsp;</li>
                     </ul>
                     <p>	For more info go to <a href="https://wordpress.org/about/requirements/" target="_blank">here</a></p>
                     <h4 id="installation_install_wordpress">Install WordPress:</h4>
                     <p>	For installtion visit this link and click <a href="https://codex.wordpress.org/Installing_WordPress" target="_blank">here</a></p>
                     <p>	&nbsp;</p>
                     <h4 id="installation_install_plugin">Install Plugin:</h4>
                     <ol>
                        <li>		<strong>WordPress Plugin Manager</strong> - please go to <i>Plugins &gt; Add new &gt; Search &quot;Support Tickets TS&quot;</i> and see the plugin then click on Install Now button. After that click on Activate.</li>
                        <li>		<strong>FTP upload</strong> - upload non-zipped plugin folder called wds to <i>/wp-content/plugins/</i> folder in your WordPress installation folder on your server.</li>
                     </ol>
                  </section>
